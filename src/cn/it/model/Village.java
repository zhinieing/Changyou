package cn.it.model;

public class Village {
	private int villageId;
	private String villageTitle;
	private String villagePoint;
	private String villageAddress;
	private String villageImg;
	private String villageInt;
	private String villageDetail;
	private int villageCount;
	private String villageSeason;
	private String villageSpecial;
	private int villageCollect;
    private float point;
	
	public float getPoint() {
		return point;
	}
	public void setPoint(float point) {
		this.point = point;
	}
	public int getVillageCollect() {
		return villageCollect;
	}
	public void setVillageCollect(int villageCollect) {
		this.villageCollect = villageCollect;
	}
	public String getVillageSeason() {
		return villageSeason;
	}
	public void setVillageSeason(String villageSeason) {
		this.villageSeason = villageSeason;
	}
	public String getVillageSpecial() {
		return villageSpecial;
	}
	public void setVillageSpecial(String villageSpecial) {
		this.villageSpecial = villageSpecial;
	}
	public int getVillageCount() {
		return villageCount;
	}
	public void setVillageCount(int villageCount) {
		this.villageCount = villageCount;
	}
	public int getVillageId() {
		return villageId;
	}
	public void setVillageId(int villageId) {
		this.villageId = villageId;
	}
	public String getVillageTitle() {
		return villageTitle;
	}
	public void setVillageTitle(String villageTitle) {
		this.villageTitle = villageTitle;
	}
	public String getVillagePoint() {
		return villagePoint;
	}
	public void setVillagePoint(String villagePoint) {
		this.villagePoint = villagePoint;
	}
	public String getVillageAddress() {
		return villageAddress;
	}
	public void setVillageAddress(String villageAddress) {
		this.villageAddress = villageAddress;
	}
	public String getVillageImg() {
		return villageImg;
	}
	public void setVillageImg(String villageImg) {
		this.villageImg = villageImg;
	}
	public String getVillageInt() {
		return villageInt;
	}
	public void setVillageInt(String villageInt) {
		this.villageInt = villageInt;
	}
	public String getVillageDetail() {
		return villageDetail;
	}
	public void setVillageDetail(String villageDetail) {
		this.villageDetail = villageDetail;
	}

}
